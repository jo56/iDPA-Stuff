public class Loops {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int x = 0;
		while (x < 100) {
			x = x + 1;
			System.out.println(x);}
		for (int i = 0; i <= 100; i++) {
			if (i % 2 == 0) {
				System.out.println(i);
				
		}else{
				System.out.println(i);
		}
	}

}
}
